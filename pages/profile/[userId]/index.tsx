import Head from 'next/head';
import { makeStyles, Theme, createStyles } from '@material-ui/core/styles';
import Button from '@material-ui/core/Button';
import { useAuth } from '../../../context/authContext';
import { db } from '../../../firebase/config';
import Aux from '../../../hoc/Auxiliary/Auxiliary';
import { UserData, GameData } from '../../../utilities/types';
import PersonalInfo from '../../../components/Profile/ProfileInfo';
import TeamIntro from '../../../components/Profile/TeamIntro';
import GameItem from '../../../components/Profile/GameItem';

const useStyles = makeStyles((theme: Theme) =>
  createStyles({
    container: {
      display: 'flex',
      flexDirection: 'row',
      flexWrap: 'wrap',
      justifyContent: 'space-around',
      alignItems: 'center',
    },
  })
);

export default function Home({
  userData,
  games,
}: {
  userData: UserData;
  games: Array<GameData>;
}) {
  const { signout } = useAuth();
  const styles = useStyles();
  return (
    <Aux>
      <Head>
        <title>Profile</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className={styles.container}>
        <PersonalInfo userData={userData} />
        <TeamIntro />
        {games.map((game, index) => {
          return <GameItem game={game} key={index} />;
        })}
        <Button onClick={signout}>Sign Out</Button>
      </div>
    </Aux>
  );
}

export async function getServerSideProps(context: {
  params: { userId: string };
}) {
  const { userId } = context.params;
  let userData = null;
  let games;
  await db
    .collection('users')
    .doc(userId)
    .get()
    .then((doc) => {
      userData = doc.data();
    })
    .catch((error) => {
      console.log('Error getting cached document:', error);
    });
  await db
    .collection('users')
    .doc(userId)
    .collection('games')
    .get()
    .then((querySnapshot) => {
      games = querySnapshot.docs.map((doc) => doc.data());
    })
    .catch((error) => {
      console.log('Error getting cached document:', error);
    });

  return {
    props: { userData, games },
  };
}
